package ruby

import "fmt"

func Println(v string) {
	fmt.Println(v)
}

func Print(v string) {
	fmt.Print(v)
}

func PrintlnInt(v int) {
	fmt.Println(v)
}

func PrintInt(v int) {
	fmt.Print(v)
}

func PrintlnFP64(v float64) {
	fmt.Println(v)
}

func PrintFP64(v float64) {
	fmt.Print(v)
}

func PrintlnFP32(v float32) {
	fmt.Println(v)
}

func PrintFP32(v float32) {
	fmt.Print(v)
}
